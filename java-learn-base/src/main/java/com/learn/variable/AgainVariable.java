package com.learn.variable;

/**
 * 重新赋值变量
 *
 * @author jinglv
 * @date 2021/5/12 5:27 下午
 */
public class AgainVariable {
    public static void main(String[] args) {
        // 定义int类型变量x，并赋予初始值100
        int x = 100;
        // 打印该变量值，观察是否为100
        System.out.println(x);
        // 重新赋值为200
        x = 200;
        // 打印该变量的值，观察是否为200
        System.out.println(x);
    }
}
